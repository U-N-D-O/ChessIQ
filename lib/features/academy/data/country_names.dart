String _normalizeCountryText(String value) {
  final lower = value.trim().toLowerCase();
  final buffer = StringBuffer();
  for (var i = 0; i < lower.length; i += 1) {
    final char = lower[i];
    buffer.write(_diacritics[char] ?? char);
  }
  return buffer.toString();
}

String? canonicalCountryName(String value) {
  final normalized = _normalizeCountryText(value);
  if (normalized.isEmpty) return null;
  final directMatch = countryNames.firstWhere(
    (name) => _normalizeCountryText(name) == normalized,
    orElse: () => '',
  );
  if (directMatch.isNotEmpty) return directMatch;
  if (countryNameAliases.containsKey(normalized)) {
    return countryNameAliases[normalized];
  }
  for (final entry in countryNameAliases.entries) {
    if (entry.key.contains(normalized) || normalized.contains(entry.key)) {
      return entry.value;
    }
  }
  final prefixMatch = countryNames.firstWhere(
    (name) => _normalizeCountryText(name).startsWith(normalized),
    orElse: () => '',
  );
  if (prefixMatch.isNotEmpty) return prefixMatch;
  return null;
}

List<String> countrySuggestions(String query) {
  final normalized = _normalizeCountryText(query);
  if (normalized.isEmpty) return const <String>[];

  final suggestions = <String>{};
  if (countryNameAliases.containsKey(normalized)) {
    suggestions.add(countryNameAliases[normalized]!);
  }

  for (final entry in countryNameAliases.entries) {
    if (entry.key.startsWith(normalized) || entry.key.contains(normalized)) {
      suggestions.add(entry.value);
    }
  }

  for (final name in countryNames) {
    final normalizedName = _normalizeCountryText(name);
    if (normalizedName.startsWith(normalized) ||
        normalizedName.contains(normalized)) {
      suggestions.add(name);
    }
  }

  return suggestions.toList()..sort((a, b) => a.compareTo(b));
}

